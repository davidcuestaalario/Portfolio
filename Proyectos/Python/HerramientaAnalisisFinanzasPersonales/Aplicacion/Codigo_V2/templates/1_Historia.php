	<hgroup id='LaHistoria' class='contenido'>
		<h2 id='LaHistoria' class='contenido'> La historia del dinero </h2>	
	</hgroup>
	
	</br>
	
	<p id='LaHistoria_Parte_1' class='Contenido'>
		Las primeras transacciones de las que se tiene constancia en la historia de la economía se llevaron a cabo durante el neolítico mediante el trueque
	</p> </br>	
	
	<p id='LaHistoria_Parte_2' class='Contenido'>
		Este sistema fue posible debido al excedente de alimentos que se produjo al sustituir la caza y la recolección por la ganadería y la agricultura que permitió que se desarrollara un sistema de división del trabajo con cierto grado de especialización.
		El trueque es un sistema mediante el cual dos partes aceptan intercambiar un conjunto de bienes y servicios por otro conjunto de bienes y servicios.
		Este sistema resulto ser ineficiente debido a que:  
		<ul class='Contenido'>
			<li class='Contenido'> 
				 No permite compensar las transacciones: Generalmente se favorezca a una de las dos partes dado que en la mayoría de las circunstancias el trueque solo se realiza por una necesidad urgente.
			</li> 
			
			<li class='Contenido'> 
				 Dificultad del intercambio: no siempre resulta posible encontrar a alguien que desee el producto o el servicio que ofrecemos para el intercambio y posea el que necesitamos. 
			</li> 
			
			<li class='Contenido'> 
				 No admite la participación de un intermediario por lo que el contacto debe darse directamente entre los dos interesados;
			</li> 
		</ul>
	</p> </br>
	
	<p id='LaHistoria_Parte_3' class='Contenido'>
		El reducido volumen de determinados productos que eran considerados valiosos por la mayoría (como el oro, la plata o la sal) simplifico el sistema del trueque utilizando dichos materiales como comodines de intercambio. 
		En algunos países este sistema se estandarizo utilizando el oro como comodín principal. Cada individuo intercambiaba los bienes y servicios que tuviera como excedente por oro que utilizaba para conseguir los bienes o servicios que más falta le hicieran. Este sistema permitió:
		<ul>
			<li class='Contenido'>
				 Un aumento significativo de las transacciones debido a que ya no era necesario necesitar algo para llevar a cabo el intercambio
			</li> 
			
			<li class='Contenido'>
				 Nació el concepto de ahorro al ser capaces de almacenar bienes no perecederos cuyo valor se mantenía constante
			</li> 
			
			<li class='Contenido'>
				 Fomento la especialización de los trabajos cubriendo una mayor cantidad de necesidades y mejorando el nivel de vida general de la población
			</li> 
		</ul>
	</p> </br>
	
	<p id='LaHistoria_Parte_4' class='Contenido'>
		El principal inconveniente de este sistema es que los bienes comodines no eran fáciles de custodiar y transportar por su volumen y peso 
		Con el tiempo aparecieron unas entidades que se comprometían a poner a buen recaudo el oro de los propietarios emitiendo certificados de posesión de unidades de bienes comodines. Estos certificados eran mucho más fáciles de transportar y almacenar que los bienes comodines a los que representaban por lo que se fomentaron más los puntos anteriormente expuestos.
		De esta forma surgió el sistema del patrón oro, un sistema monetario en el que el medio interno de intercambio de la economía no tiene un valor intrínseco, pero es aceptada porque las entidades de custodia o los gobiernos se comprometen a intercambiarla por una cantidad de oro preestablecida. 
		Mediante este sistema:
		<ul class='Contenido'>
			<li class='Contenido'>
				 No se podían imprimir más moneda que aquella que pudieran respaldar con oro por lo que:
				<ul class='Contenido'>
					<li class='Contenido_2'>
						El volumen de moneda en circulación dependía únicamente de las actividades mineras
					</li> 
					
					<li class='Contenido_2'>
						Existían tantos certificados de una entidad de custodia como oro tenía almacenado
					</li> 
				</ul>
			</li> 
			
			<li class='Contenido'>
			 Los banqueros no tenían la potestad de prestar los bienes que custodiaban. 
			</li> 
			
			<li class='Contenido'>
			 Si algún individuo acudía a alguna de estas entidades podía intercambiar instantáneamente sus certificados por los bienes comodines.
			</li>
		</ul>
	</p> </br>

	<p id='LaHistoria_Parte_5' class='Contenido'>
		No obstante, debido a que intercambiar los certificados por oro no era lo habitual, las entidades que se dedicaban a custodiar los bienes comodines comenzaron a emitir más certificados de los que podían respaldar con la finalidad de prestárselos a individuos que con el tiempo pudieran devolverlos con un interés momento en el que destruían los certificados sin respaldo.
		Este sistema se caracterizaba porque  
		<ul class='Contenido'>
			<li class='Contenido'>
				 Existían muchas entidades de custodia que podían intercambiaban libremente sus reservas de oro y sus certificados.
			</li> 
			
			<li class='Contenido'>
				 Permite crear y destruir dinero libremente
			</li> 
			
			<li class='Contenido'>
				 Estaban obligadas a intercambiar los certificados por oro y si no podía hacer frente a los intercambios se declaraba en bancarrota
			</li>
		</ul>
	</p> </br>
	
	<p id='LaHistoria_Parte_6' class='Contenido'>
		Más adelante los gobiernos decidieron centralizar este sistema, para ello:
		<ul class='Contenido'>
			<li class='Contenido'>		
				 Prohibieron a todas las entidades de custodia emitir nuevos certificados 
			</li> 
			
			<li class='Contenido'>
				 Sustituyeron todos los certificados que estaban en circulación por otros certificados del gobierno
			</li> 
			
			<li class='Contenido'>
				El oro custodiado por los bancos privados paso a ser propiedad del gobierno creando el banco central
			</li> 
			
			<li class='Contenido'>
				 Los bancos privados pasaron a ser sucursales del banco central creando el sistema de reservas fraccionarias
			</li> 
			
			<li class='Contenido'>
				 Estaban obligadas a intercambiar los certificados por oro, pero modificaban el número de certificados que se intercambiaba por cada oro en función del número de certificados que se habían emitido de modo que no fuera posible quedarse en bancarrota
			</li>
		</ul>
	</p> </br>
	
	<p id='LaHistoria_Parte_7' class='Contenido'>
	Finalmente, el gobierno desvinculo el oro de la moneda, de modo que ya no era posible intercambiar certificados por oro en el banco central
	</p> </br>
	
