	<hgroup id='ElJuego' class='contenido'>
		<h2 id='ElJuego' class='contenido'> El juego del dinero </h2>	
	</hgroup>
	
	</br>
	
	<p id='ElJuego_Parte_1' class='Contenido'>
		
		Las personas más ricas y poderosas del mundo creen, efectivamente, que el dinero es un juego. 
		¿Por qué otro motivo iba a trabajar alguien diez o doce horas diarias cuando ya ha ganado miles de millones de dólares?
		Los ricos saben que el dinero es una ilusión basada en la confianza de las masas.
	</p>	
	
	</br>
	
	<p id='ElJuego_Parte_2' class='Contenido'>
		Mientras que algunas personas los consume una sed de dinero tan grande que los destruye,
		los ricos ven el dinero como una herramienta y no como un fin.
		Como cualquier otra herramienta se puede utilizar para hacer cosas buenas y cosas malas. 
		Al igual que un martillo se puede utilizar para construir,
		también se puede utilizar destruir. 
	</p>
	
	</br>
	
	<p id='ElJuego_Parte_3' class='Contenido'>
		La diferencia entre el dinero y cualquier otra herramienta es su versatilidad.
		El dinero es una herramienta que tiene la capacidad de imitar las cualidades de cualquier otra herramienta. 
		De ahí su mayor potencial para construir o destruir. 
		Mientras que con un martillo solo se pueden clavar clavos, 
		el dinero se puede utilizar para comprar un martillo o una sierra y cortar tablas.
		E incluso para contratar a un carpintero que nos proporcione temporalmente unas habilidades que, de otro modo, nunca habríamos tenido.
	</p>
	
	</br>
	
	<p id='ElJuego_Parte_4' class='Contenido'>
		Una herramienta tan potente tiene la capacidad de construir más cosas que cualquier otra herramienta. 
		Lo que sucede es que las herramientas con mayor potencial son más difíciles de utilizar correctamente. 
		Combinado con la gran cantidad de desinformación que existe al respecto y la falta de educación financiera, 
		muy pocas personas son capaces de desatar el verdadero potencial del dinero.
	</p>
	
	</br>
	
	<p id='ElJuego_Parte_5' class='Contenido'>
		Mucha gente ve el dinero como un tema tabú del que no se puede hablar.
		No obstante, el dinero es simplemente un medio para satisfacer nuestros deseos y necesidades, 
		sí profundizamos en lo que realmente perseguimos, veremos que no es dinero. 
		Lo que realmente perseguimos es aquello que creemos que el dinero nos dará. 
		En última instancia, es una serie de sentimientos. Y bajo esos sentimientos hay necesidades.
		Y una cosa es segura. O lo usamos o nos usa. O dominamos el dinero o, de alguna manera, el dinero nos dominara a nosotros.
	</p>
	
	</br>
	
	<hgroup id='LibertadFinanciera' class='contenido'>
		<h2 id='LibertadFinanciera' class='contenido'> La Libertad Financiera </h2>	
	</hgroup>
	
	</br>
	
	<p id='ElJuego_Parte_1' class='Contenido'>
		¿Y si el dinero no importara? 
		¿Cómo nos sentiríamos si no tuviéramos que preocuparnos por ir todas las mañanas a una oficina, 
		por pagar las facturas ni por tener que ahorrar para la jubilación? 
		¿Cómo sería vivir como nos diera la gana? La verdad es que muchos seguiríamos trabajando. 
		Pero lo haríamos con alegría y por gusto. Nuestro trabajo continuaría, pero el estrés y la ansiedad desaparecerían. 
		Trabajaríamos porque queremos, no porque tenemos que hacerlo. 
		Eso es la libertad financiera.
	</p>
	
